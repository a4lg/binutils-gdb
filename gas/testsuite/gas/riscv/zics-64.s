target:
	## Register-immediate addressing (with pre/post update)
	lwu	a0, (+a1)
	lwu	a0, (a1+)
	lwu	a0, 60(+a1)
	lwu	a0, 60(a1+)
	lwu	a0, -64(+a1)
	lwu	a0, -64(a1+)
	ld	a0, (+a1)
	ld	a0, (a1+)
	ld	a0, 120(+a1)
	ld	a0, 120(a1+)
	ld	a0, -128(+a1)
	ld	a0, -128(a1+)

	## Register-register addressing (with optional pre/post update), non-scaled
	lwu	a0, a2(a1)
	lwu	a0, a2(+a1)
	lwu	a0, a2(a1+)
	ld	a0, a2(a1)
	ld	a0, a2(+a1)
	ld	a0, a2(a1+)
	sd	a0, a2(a1)
	sd	a0, a2(+a1)
	sd	a0, a2(a1+)
	# rd == rs1 is valid on stores and no update load variants
	lwu	a0, a2(a0)
	ld	a0, a2(a0)
	sd	a0, a2(a0)
	sd	a0, a2(+a0)
	sd	a0, a2(a0+)

	## Register-register addressing (with optional pre/post update), scaled
	lwu	a0, [a2](a1)
	lwu	a0, [a2](+a1)
	lwu	a0, [a2](a1+)
	ld	a0, [a2](a1)
	ld	a0, [a2](+a1)
	ld	a0, [a2](a1+)
	sd	a0, [a2](a1)
	sd	a0, [a2](+a1)
	sd	a0, [a2](a1+)
	# rd == rs1 is valid on stores and no update load variants
	lwu	a0, [a2](a0)
	ld	a0, [a2](a0)
	sd	a0, [a2](a0)
	sd	a0, [a2](+a0)
	sd	a0, [a2](a0+)

	## Pair load/stores
	lwup	a0, a1, (sp)
	lwup	a0, a1, (+sp)
	lwup	a0, a1, (sp+)
	lwup	a0, a1, 60(sp)
	lwup	a0, a1, 60(+sp)
	lwup	a0, a1, 60(sp+)
	lwup	a0, a1, -64(sp)
	lwup	a0, a1, -64(+sp)
	lwup	a0, a1, -64(sp+)
	ldp	a0, a1, (sp)
	ldp	a0, a1, (+sp)
	ldp	a0, a1, (sp+)
	ldp	a0, a1, 120(sp)
	ldp	a0, a1, 120(+sp)
	ldp	a0, a1, 120(sp+)
	ldp	a0, a1, -128(sp)
	ldp	a0, a1, -128(+sp)
	ldp	a0, a1, -128(sp+)
	sdp	a0, a1, (sp)
	sdp	a0, a1, (+sp)
	sdp	a0, a1, (sp+)
	sdp	a0, a1, 120(sp)
	sdp	a0, a1, 120(+sp)
	sdp	a0, a1, 120(sp+)
	sdp	a0, a1, -128(sp)
	sdp	a0, a1, -128(+sp)
	sdp	a0, a1, -128(sp+)
	# rs1 == sp || rs2 == sp (but rs1 != rs2) is valid on no update variants
	lwup	sp, a0, (sp)
	lwup	a0, sp, (sp)
	ldp	sp, a0, (sp)
	ldp	a0, sp, (sp)
	# rs1 == rs2 is valid on stores
	sdp	a0, a0, (sp)
	sdp	a0, a0, (+sp)
	sdp	a0, a0, (sp+)
